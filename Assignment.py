# Assignment operator
"""
=   Assignment
+=  addition
-=  Substaction
*=  Multiplication
/=  Division
%=  Modulus
**= Exponentiation
//= Floor division
"""
a = 5
print(a)

a += 5   #a = a+5
print(a)
a -= 10  #a = a-10
print(a)
a *= 10  #a = a*10
print(a)
a /= 10  #a = a/10
print(a)
a %= 10  #a = a%10
print(a)

